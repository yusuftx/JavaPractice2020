package day2;

public class primitiveDataTypes {
	
	public static void main(String[] args) {
		// float 4 bytes
		float num5 = 107.5f;
		System.out.println(num5);
		
		// double 8 bytes
		double num6 = 50753.5;
		double num7 = 67864;
		System.out.println(num6);
		System.out.println(num7);
		
	}
	
}
